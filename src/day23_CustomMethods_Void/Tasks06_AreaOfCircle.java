public class Tasks06_AreaOfCircle {
    public static void main(String[] args) {
        areaOfCircle(5);
    }
    public static void areaOfCircle(double r){
        double area = 3.14*r*r;
        System.out.println("Area of circle: "+ area);
    }
}
